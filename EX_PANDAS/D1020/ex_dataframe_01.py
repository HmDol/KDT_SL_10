## ----------------------------------------------------------------------
## DataFrame 인스턴스/객체 생성
## ----------------------------------------------------------------------
## 모듈 로딩
## ----------------------------------------------------------------------
import pandas as pd

## ----------------------------------------------------------------------
## -> 데이터 준비
## ----------------------------------------------------------------------
jumsu = [[99, 77, 55], [78,12,33], [98,23,91]]

## ----------------------------------------------------------------------
## -> Series로 변환 저장
## ----------------------------------------------------------------------
df = pd.DataFrame(jumsu)

## ----------------------------------------------------------------------
## -> List와 Series 데이터 표현/저장 방식 비교
## ----------------------------------------------------------------------
print('------[List]-------')
print(jumsu)

print('\n--------[DataFrame 방식]----------')
print(df)

## ----------------------------------------------------------------------
## -> Series 인스턴스의 속성 읽기
## ----------------------------------------------------------------------
print("\n--------------속성 읽기 -------------------")
print('인덱스 :',df.index)
print('컬럼즈 :',df.columns)
print(f'데이터 : {df.values}, {type(df.values)}')
print(f'형  태 : {df.shape}') ## 1차원으로 원소 수 반환
print(f'차  원 : {df.ndim}') ## 1차원
print(f'타  입 : {df.dtypes}')