## ----------------------------------------------------------------------
## Series 인스턴스/객체 생성
## ----------------------------------------------------------------------
## 모듈 로딩
## ----------------------------------------------------------------------
import pandas as pd

## ----------------------------------------------------------------------
## -> 데이터 준비
## ----------------------------------------------------------------------
jumsu = [99, 77, 55, 88, 66]

## ----------------------------------------------------------------------
## -> Series로 변환 저장
## ----------------------------------------------------------------------
sr = pd.Series(jumsu)
# print(sr)


## ----------------------------------------------------------------------
## -> List와 Series 데이터 표현/저장 방식 비교
## ----------------------------------------------------------------------
print('------[List]-------')
print(jumsu)

print('\n--------[Series 방식]----------')
print(sr)

## ----------------------------------------------------------------------
## -> Series 인스턴스의 속성 읽기
## ----------------------------------------------------------------------
print("\n--------------속성 읽기 -------------------")
print('인덱스 :',sr.index)
print(f'데이터 : {sr.values}, {type(sr.values)}')
print(f'형  태 : {sr.shape}') ## 1차원으로 원소 수 반환
print(f'차  원 : {sr.ndim}') ## 1차원
print(f'타  입 : {sr.dtype}')