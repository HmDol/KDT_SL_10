## ----------------------------------------------------------------------
## Series 인스턴스/객체 생성
## ----------------------------------------------------------------------
## 모듈 로딩
## ----------------------------------------------------------------------
import pandas as pd


## ----------------------------------------------------------------------
## -> Series 인스턴스의 속성 읽기 함수
## ----------------------------------------------------------------------
def print_attr(sr, name) :
    print("\n--------------", name , "속성 읽기 -------------------")
    print('인덱스 :',sr.index)
    print(f'데이터 : {sr.values}, {type(sr.values)}')
    print(f'형  태 : {sr.shape}') ## 1차원으로 원소 수 반환
    print(f'차  원 : {sr.ndim}') ## 1차원
    print(f'타  입 : {sr.dtype}')


## ----------------------------------------------------------------------
## -> 데이터 준비
## ----------------------------------------------------------------------
jumsu = [99, 77, 55, 88, 66]
idx = ['국어', '영어', '미술', '과학', '수학']

## ----------------------------------------------------------------------
## -> Series로 변환 저장
## ----------------------------------------------------------------------
sr1 = pd.Series(jumsu,idx)
print(sr1)

sr2 = pd.Series(jumsu, idx, dtype=pd.Int8Dtype())
print(sr2)

sr3 = pd.Series(jumsu, idx, dtype='int8')
print(sr3)

sr4 = pd.Series(jumsu, idx, dtype='int8', name='jumsu')
print(sr4)


## ----------------------------------------------------------------------
## -> List와 Series 데이터 표현/저장 방식 비교
## ----------------------------------------------------------------------
print_attr(sr1, "sr1")

print_attr(sr2, 'sr2')

print_attr(sr3, 'sr3')

print_attr(sr4, 'sr4')
