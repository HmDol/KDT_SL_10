void n1() {
	int n1, n2, n3;
	printf("세 정수를 입력하세요(ex 1 2 3) : ");
	if (scanf("%d %d %d", &n1, &n2, &n3) != 3) { printf("잘못된 접근"); return 0; }
	int max, min;
	(n1 >= n2) ? ((n1 >= n3) ? (max = n1) : (max = n3)) : ((n2 >= n3) ? (max = n2) : (max = n3));
	(n1 <= n2) ? ((n1 <= n3) ? (min = n1) : (min = n3)) : ((n2 <= n3) ? (min = n2) : (min = n3));

	printf("max = %d / min = %d", max, min);
}
