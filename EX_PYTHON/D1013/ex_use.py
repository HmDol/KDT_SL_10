## 모듈 로딩
import ex_test as t

## ------------------------------------------------------
## 인스턴스/객체 생성 => 변수명 = 클래스명()
## ------------------------------------------------------
## 학생 인스턴스/객체 생성
s1 = t.Student('knu','21812080', '컴퓨터', '홍길동', 26, 4, 90)
s2 = t.Student('ynu','21812110', '컴퓨터', '김철수', 24, 3, 100)

## ------------------------------------------------------
## 인스턴스/객체 활용 => 변수명.속성명 / 변수명.메서드명()
## ------------------------------------------------------
s1.printStudent()

## ------------------------------------------------------
## 인스턴스/객체 활용 => 변수명.속성명 = 새로운 값
## ------------------------------------------------------
s1.grade = 5
s1.printStudent()

## 2명 학생의 성적에 대한 평균 출력
avg = (s1 + s2) / 2
print(f'평균 : {avg}')
