## 키 이벤트 처리
##  - 이벤트 소스(Widget) -- 이벤트 처리함수(핸들러/리스너) : 연결(Bind)
##  - 이벤트 종류/타입
##      * <Return>  : 엔터키 눌렀을 때
##      * <Pause>   : Pause 키 눌렀을 때
##      * <Delete>  : del키 눌렀을 때
