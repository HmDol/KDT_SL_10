## ------------------------------------------------------------------==
## 모듈 사용하기
## - 모듈 : 특정 목적에 관련된 변수, 함수, 클래스 등이 있는 파일(py)
## - 사용 : import 모듈명
## ------------------------------------------------------------------==
## 모듈 로딩
## ------------------------------------------------------------------==
from math import pi, factorial    # 특정 함수만 로딩, 함수명만 사용 가능
from random import *              # 모듈 내 모든 것을 로딩, 함수명만 사용 가능

## ------------------------------------------------------------------==
## 전역변수
## ------------------------------------------------------------------==
pi = "파이값 3.14"

## ------------------------------------------------------------------==
## 모듈 일부분 => 변수명, 함수명(), 클래스명
## ------------------------------------------------------------------==
print(f'pi => {pi}')  ## 코드 내 변수 우선!
print(f'factorial(3) => {factorial(3)}')
print(f'random() => {random()}')
print(f'randint(1,6) => {randint(1,6)}')


