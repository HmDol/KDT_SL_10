#     tm_hour=13, 
#     tm_min=8, 
#     tm_sec=34, 
#     tm_wday=1, 
#     tm_yday=287, 
#     tm_isdst=0
#     )

t = time.localtime()
print("현재 시간 구조체\n", t, sep="")
print()
print("년 : ", t.tm_year, "/ 월 :", t.tm_mon, "/ 일 :", t.tm_mday)
print()

## => 시간 포맷팅
print("포맷된 시간:", time.strftime("%Y-%m-%d %H:%M:%S", t))

## => str을 timedmfh 변환
s = "2025-10-02 14:30:00"
parsed = time.strptime(s, "%Y-%m-%d %H:%M:%S")
print("파싱된 시간 구조체\n", parsed, sep="")
print()

## => 프로그램 지연/일시정지(sleep(sec))
print("3초 대기")
time.sleep(3)
print("대기 끝")


